int soundplus_deal_Aec(float *out, short *in, short *ref, int buf_len, int ref_len)
{
    int i;
    int numsamples = buf_len/MIC_NUM;
    
    if(ref_len == FRMLEN)
    {
    	memmove(pRef_align, pRef_align+FRMLEN, sizeof(short)*DELAY);
    	memcpy(pRef_align+DELAY, ref, sizeof(short)*FRMLEN);
	}
	else
	{
		memmove(pRef_align, pRef_align+FRMLEN/2, sizeof(short)* DELAY_NB);
		memcpy(pRef_align+ DELAY_NB, ref, sizeof(short)*FRMLEN/2);
	}

    for(i=0; i<numsamples; i++)
	{
		pMic_In[i] = in[MIC_NUM*i+0];
		pMic_In[numsamples + i] = in[MIC_NUM*i+1];
		pMic_In[numsamples*2 + i] = in[MIC_NUM*i+2];
	}

    for(i=0; i<ref_len; i++)
    {
        pRef_In[i] = pRef_align[i];
    }

    Sndp_SpxEnh_Aec(out, pMic_In, pRef_In, numsamples, ref_len);

    return 0;
}

